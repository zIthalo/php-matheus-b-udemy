<?php 
/*
    -podemos colocar uma str de trás para frente com a função strrev() (algo como string reverse);

*/

$araujo = "ojuara";

$ojuara = strrev($araujo);

$alfabeto = "a b c d e f g h i j k l m n o p q r s t u v w x y z";

$otebafla = strrev($alfabeto);

echo "$ojuara <br>";

echo "<h3>Alfabeto: $alfabeto </h3>";
echo "<h3>Alfabeto de trás para frente: $otebafla </h3>"

?>
